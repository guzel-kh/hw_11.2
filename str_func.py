def str_upper(string: str):
    """
    возвращает строку со всеми заглавными буквами
    :param string: str
    :return: str
    """
    return string.upper


def str_titled(string: str):
    """
    делает заглавными первые буквы каждого слова в строке
    :param string: str
    :return: str
    """
    return string.title()
